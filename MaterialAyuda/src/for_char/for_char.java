package for_char;

public class for_char {

	public static void main(String[] args) {

		for (char letra = 'A'; letra <= 'Z'; letra++) {
			System.out.println(letra);
			/*
			 * En este ejemplo, el bucle for se inicializa con letra = 'A', y se ejecutará
			 * siempre que letra sea menor o igual a 'Z'. En cada iteración, se imprimirá el
			 * valor actual de letra. El bucle se ejecutará desde la letra 'A' hasta la
			 * letra 'Z', imprimiendo todas las letras del alfabeto en mayúsculas. Esto es
			 * posible, ya que las computadoras almacenan las letras asociadas a valores
			 * númericos (código ASCII). Por ejemplo: 'A' está asociada al número 65,
			 * entonces se le puede sumar 1, y al obtener 66, pasamos a la letra 'B'.
			 */
		}

	}

}
